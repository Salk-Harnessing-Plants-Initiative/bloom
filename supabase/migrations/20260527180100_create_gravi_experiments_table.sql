-- References to species, people,
-- accessions resolve against V2's existing tables. scientist_id targets
-- phenotypers (V2's people-equivalent for legacy scientist records) to match
-- V1's behaviour where the desktop RPC upserts a scientist row keyed by email.

-- scientist_id references cyl_scientists (not phenotypers) — matches V1 prod
-- schema where the desktop RPC upserts a scientist row in cyl_scientists keyed
-- by email. system_name lets the same experiment name coexist across multiple
-- GraviScan rigs (e.g. "GRAV-01" and "GRAV-02"); UNIQUE is on the triple.

CREATE TABLE gravi_experiments (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    species_id BIGINT REFERENCES species(id),
    scientist_id BIGINT REFERENCES cyl_scientists(id),
    accession_id BIGINT REFERENCES accessions(id),
    system_name TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE(species_id, name, system_name)
);

ALTER TABLE gravi_experiments ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS admin_all_gravi_experiments ON public.gravi_experiments;
CREATE POLICY admin_all_gravi_experiments ON public.gravi_experiments
    FOR ALL TO bloom_admin USING (true) WITH CHECK (true);

DROP POLICY IF EXISTS agent_read_gravi_experiments ON public.gravi_experiments;
